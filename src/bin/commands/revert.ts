/**
 * Revert command — deterministic rollback of a merged PR.
 *
 * Flow: resolve target PR → git revert → verify → create revert PR
 * No LLM needed — the revert is a deterministic git operation.
 *
 * Usage:
 *   @kody revert #87           — revert PR #87
 *   @kody revert               — revert the PR linked to the current issue
 *   kody-engine revert --target 87 --issue-number 42
 */

import { logger } from "../../logger.js"
import { getProjectConfig } from "../../config.js"
import {
  getDefaultBranch,
  getCurrentBranch,
  checkoutBranch,
  createBranch,
  pushBranch,
  revertCommit,
} from "../../git-utils.js"
import {
  getMergedPRDetails,
  findMergedPRForIssue,
  createPR,
  postComment,
  setLabel,
} from "../../github-api.js"
import { runQualityGates, type VerifyResult } from "../../verify-runner.js"

// ─── Types ──────────────────────────────────────────────────────────────────

export interface RevertInput {
  /** PR or issue number to revert (as string, parsed from CLI) */
  target?: string
  /** Issue context — for PR resolution and commenting */
  issueNumber?: number
  dryRun: boolean
  cwd?: string
}

interface RevertTarget {
  prNumber: number
  prTitle: string
  mergeCommitSha: string
  headBranch: string
  baseBranch: string
}

// ─── Pure functions ─────────────────────────────────────────────────────────

/** Derive branch name for the revert PR. */
export function deriveRevertBranchName(prNumber: number, prTitle: string): string {
  const slug = prTitle
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .slice(0, 40)
    .replace(/-$/, "")
  return `revert/${prNumber}-${slug}`
}

/** Build the PR body for a revert PR. */
export function buildRevertPrBody(
  target: RevertTarget,
  verifyResult: VerifyResult | null,
  issueNumber?: number,
): string {
  const sections: string[] = []

  sections.push(`## Revert PR #${target.prNumber}`)
  sections.push(`\nThis reverts the changes from **${target.prTitle}** (PR #${target.prNumber}).`)
  sections.push(`\nOriginal merge commit: \`${target.mergeCommitSha.slice(0, 7)}\``)

  if (verifyResult) {
    const icon = verifyResult.pass ? "✅" : "⚠️"
    sections.push(`\n**Verify:** ${icon} ${verifyResult.pass ? "passed" : "failed (see errors below)"}`)
    if (!verifyResult.pass && verifyResult.errors.length > 0) {
      sections.push(`\n<details><summary>Verify errors</summary>\n`)
      for (const e of verifyResult.errors) {
        sections.push(`- ${e}`)
      }
      sections.push(`\n</details>`)
    }
  }

  if (issueNumber) {
    sections.push(`\nRelated to #${issueNumber}`)
  }

  sections.push(`\n---\n🤖 Generated by Kody`)

  return sections.join("\n")
}

// ─── Orchestration ──────────────────────────────────────────────────────────

export async function revertCommand(input: RevertInput): Promise<void> {
  const cwd = input.cwd ?? process.cwd()
  const prefix = input.dryRun ? "[dry-run] " : ""

  logger.info(`${prefix}Starting revert command...`)

  // ── Step 1: Resolve the target PR ─────────────────────────────────────
  let targetPR: number | undefined

  if (input.target) {
    targetPR = parseInt(input.target.replace(/^#/, ""), 10)
    if (isNaN(targetPR)) {
      logger.error(`Invalid revert target: ${input.target}`)
      process.exit(1)
    }
  } else if (input.issueNumber) {
    logger.info(`  No target specified, searching for merged PR linked to issue #${input.issueNumber}...`)
    const linked = findMergedPRForIssue(input.issueNumber)
    if (linked) {
      targetPR = linked.number
      logger.info(`  Found linked PR #${linked.number}: ${linked.title}`)
    }
  }

  if (!targetPR) {
    const msg = input.target
      ? `Could not resolve revert target: ${input.target}`
      : `No merged PR found for issue #${input.issueNumber ?? "?"}. Use \`kody revert #<PR>\` (prefix with @) to specify explicitly.`
    logger.error(msg)
    if (input.issueNumber && !input.dryRun) {
      try { postComment(input.issueNumber, `❌ Revert failed: ${msg}`) } catch { /* best effort */ }
    }
    process.exit(1)
  }

  // ── Step 2: Get PR details + merge commit SHA ─────────────────────────
  logger.info(`  Looking up PR #${targetPR}...`)
  const prDetails = getMergedPRDetails(targetPR)
  if (!prDetails) {
    const msg = `PR #${targetPR} is not merged or not found`
    logger.error(msg)
    if (input.issueNumber && !input.dryRun) {
      try { postComment(input.issueNumber, `❌ Revert failed: ${msg}`) } catch { /* best effort */ }
    }
    process.exit(1)
  }

  if (!prDetails.mergeCommitSha) {
    const msg = `PR #${targetPR} has no merge commit SHA`
    logger.error(msg)
    if (input.issueNumber && !input.dryRun) {
      try { postComment(input.issueNumber, `❌ Revert failed: ${msg}`) } catch { /* best effort */ }
    }
    process.exit(1)
  }

  const target: RevertTarget = {
    prNumber: targetPR,
    prTitle: prDetails.title,
    mergeCommitSha: prDetails.mergeCommitSha,
    headBranch: prDetails.headBranch,
    baseBranch: prDetails.baseBranch,
  }

  logger.info(`  Target: PR #${target.prNumber} "${target.prTitle}"`)
  logger.info(`  Merge commit: ${target.mergeCommitSha.slice(0, 7)}`)
  logger.info(`  Base branch: ${target.baseBranch}`)

  // ── Step 3: Create revert branch ──────────────────────────────────────
  const revertBranch = deriveRevertBranchName(target.prNumber, target.prTitle)
  logger.info(`${prefix}Creating branch: ${revertBranch}`)

  if (!input.dryRun) {
    const defaultBranch = getDefaultBranch(cwd)
    const current = getCurrentBranch(cwd)
    if (current !== defaultBranch) {
      checkoutBranch(defaultBranch, cwd)
    }
    createBranch(revertBranch, cwd)
  }

  // ── Step 4: Git revert ────────────────────────────────────────────────
  logger.info(`${prefix}Reverting commit ${target.mergeCommitSha.slice(0, 7)}...`)

  if (!input.dryRun) {
    // Try as merge commit first (most PRs), falls back to non-merge automatically
    const result = revertCommit(target.mergeCommitSha, true, cwd)
    if (!result.success) {
      const msg = `Revert failed: ${result.error}`
      logger.error(msg)
      if (input.issueNumber) {
        try { postComment(input.issueNumber, `❌ ${msg}`) } catch { /* best effort */ }
      }
      process.exit(1)
    }
    logger.info(`  Revert commit created successfully`)
  }

  // ── Step 5: Verify ────────────────────────────────────────────────────
  let verifyResult: VerifyResult | null = null
  logger.info(`${prefix}Running verification (typecheck + lint + tests)...`)

  if (!input.dryRun) {
    verifyResult = runQualityGates("", cwd)
    if (verifyResult.pass) {
      logger.info(`  Verify: PASS`)
    } else {
      logger.warn(`  Verify: FAIL (proceeding with PR anyway)`)
      for (const e of verifyResult.errors) {
        logger.warn(`    ${e}`)
      }
    }
  }

  // ── Step 6: Push + Create PR ──────────────────────────────────────────
  const prTitle = `revert: ${target.prTitle} (#${target.prNumber})`.slice(0, 72)
  const prBody = buildRevertPrBody(target, verifyResult, input.issueNumber)
  const config = getProjectConfig()
  const baseBranch = config.git?.defaultBranch ?? target.baseBranch

  logger.info(`${prefix}Pushing branch and creating PR...`)
  logger.info(`  Title: ${prTitle}`)
  logger.info(`  Base: ${baseBranch}`)

  if (!input.dryRun) {
    pushBranch(cwd)
    const pr = createPR(revertBranch, baseBranch, prTitle, prBody)

    if (pr) {
      logger.info(`  PR created: ${pr.url}`)

      // Label the PR
      try { setLabel(pr.number, "revert") } catch { /* best effort */ }

      // Comment on the original issue
      if (input.issueNumber) {
        try {
          postComment(input.issueNumber, `🔄 Revert PR created: ${pr.url}\n\nReverts PR #${target.prNumber}: "${target.prTitle}"`)
        } catch { /* best effort */ }
      }
    } else {
      logger.error(`  Failed to create PR`)
      if (input.issueNumber) {
        try { postComment(input.issueNumber, `❌ Revert: pushed branch \`${revertBranch}\` but failed to create PR`) } catch { /* best effort */ }
      }
    }
  }

  logger.info(`${prefix}Revert command completed`)
}
